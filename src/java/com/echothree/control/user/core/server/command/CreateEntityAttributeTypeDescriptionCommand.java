package com.echothree.control.user.core.server.command;

import com.echothree.control.user.core.remote.form.CreateEntityAttributeTypeDescriptionForm;
import com.echothree.model.control.core.server.CoreControl;
import com.echothree.model.control.party.server.PartyControl;
import com.echothree.model.data.core.server.entity.EntityAttributeType;
import com.echothree.model.data.core.server.entity.EntityAttributeTypeDescription;
import com.echothree.model.data.party.server.entity.Language;
import com.echothree.model.data.user.remote.pk.UserVisitPK;
import com.echothree.util.common.message.ExecutionErrors;
import com.echothree.util.common.validation.FieldDefinition;
import com.echothree.util.common.validation.FieldType;
import com.echothree.util.remote.command.BaseResult;
import com.echothree.util.server.control.BaseSimpleCommand;
import com.echothree.util.server.persistence.Session;
import java.util.Arrays;
import java.util.Collections;
import java.util.List;

public class CreateEntityAttributeTypeDescriptionCommand
        extends BaseSimpleCommand<CreateEntityAttributeTypeDescriptionForm> {
    
    private final static List<FieldDefinition> FORM_FIELD_DEFINITIONS;
    
    static {
        FORM_FIELD_DEFINITIONS = Collections.unmodifiableList(Arrays.asList(
                new FieldDefinition("EntityAttributeTypeName", FieldType.ENTITY_NAME, true, null, null),
                new FieldDefinition("LanguageIsoName", FieldType.ENTITY_NAME, true, null, null),
                new FieldDefinition("Description", FieldType.STRING, true, 1L, 80L)
                ));
    }
    
    /** Creates a new instance of CreateEntityAttributeTypeDescriptionCommand */
    public CreateEntityAttributeTypeDescriptionCommand(UserVisitPK userVisitPK, CreateEntityAttributeTypeDescriptionForm form) {
        super(userVisitPK, form, null, FORM_FIELD_DEFINITIONS, false);
    }
    
    @Override
    protected BaseResult execute() {
        CoreControl coreControl = getCoreControl();
        String entityAttributeTypeName = form.getEntityAttributeTypeName();
        EntityAttributeType entityAttributeType = coreControl.getEntityAttributeTypeByName(entityAttributeTypeName);
        
        if(entityAttributeType != null) {
            PartyControl partyControl = (PartyControl)Session.getModelController(PartyControl.class);
            String languageIsoName = form.getLanguageIsoName();
            Language language = partyControl.getLanguageByIsoName(languageIsoName);
            
            if(language != null) {
                EntityAttributeTypeDescription entityAttributeTypeDescription = coreControl.getEntityAttributeTypeDescription(entityAttributeType, language);
                
                if(entityAttributeTypeDescription == null) {
                    String description = form.getDescription();
                    
                    coreControl.createEntityAttributeTypeDescription(entityAttributeType, language, description);
                } else {
                    addExecutionError(ExecutionErrors.DuplicateEntityAttributeTypeDescription.name(), entityAttributeTypeName, languageIsoName);
                }
            } else {
                addExecutionError(ExecutionErrors.UnknownLanguageIsoName.name(), languageIsoName);
            }
        } else {
            addExecutionError(ExecutionErrors.UnknownEntityAttributeTypeName.name(), entityAttributeTypeName);
        }
        
        return null;
    }
    
}
